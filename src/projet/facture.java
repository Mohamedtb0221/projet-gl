    public double[] total(String nom){
        SimpleDateFormat dcn = new SimpleDateFormat("yyyy-MM-dd");
        double[] a = new double[2];
        double tot=0;
        nom=nompre.getText();
        try{
            Connection conn =DriverManager.getConnection("jdbc:mysql://localhost/projet", "root", "medtb21");
            
            st =conn.createStatement();
            rs=st.executeQuery("select prix,qttP from commande where nomprenom ='"+nom+"' and date = '"+dcn.format(date.getDate())+"'");
            
            while(rs.next()){
                tot=tot+rs.getInt("qttP")*rs.getInt("prix");
            }
            a[0]=tot;
            if(tot>3000){
                a[1]=tot-(tot*0.1);
            }
            conn.close();
        }catch(SQLException e){
            e.printStackTrace();
        }
        return a;
    }
