    private void ajtcommandeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ajtcommandeMouseClicked
        try{

            Connection conn =DriverManager.getConnection("jdbc:mysql://localhost/projet", "root", "medtb21");
            PreparedStatement add=conn.prepareStatement("insert into commande values (?,?,?,?,?,?,?,?,?)");
            if(nompre.getText().isEmpty()||adr.getText().isEmpty()||email.getText().isEmpty()||reference.getText().isEmpty()||designation.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "veilluez remplir tout les information de votre commande !");
            }else
            if(Integer.valueOf(qtt.getValue().toString())==0){
                JOptionPane.showMessageDialog(this, "la quantité ne peut pas etre 0 !");
            }else{
                SimpleDateFormat dcn = new SimpleDateFormat("yyyy-MM-dd");
            add.setString(1,nompre.getText());
            add.setString(2,adr.getText());
            add.setString(3,email.getText());
            add.setString(4,rev.getSelectedItem().toString());
            add.setInt(5,Integer.valueOf(reference.getText()));
            add.setString(6,designation.getText());
            add.setInt(7,Integer.valueOf(qtt.getValue().toString()));
            add.setInt(8,Integer.valueOf(prix.getText()));
            add.setString(9,dcn.format(date.getDate()));
            
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "commande ajouté !");
            total.setText(String.valueOf(total(nompre.getText())[0]));
            red.setText(String.valueOf(total(nompre.getText())[1]));
            
            
            conn.close();
            
            selectprod();}
        }catch(SQLException e){
            e.printStackTrace();
        }
        

    }//GEN-LAST:event_ajtcommandeMouseClicked

