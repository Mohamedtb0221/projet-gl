    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
         try{

            Connection conn =DriverManager.getConnection("jdbc:mysql://localhost/projet", "root", "medtb21");
            PreparedStatement add=conn.prepareStatement("insert into plandevente values (?,?,?)");
            if(estim.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "veuillez entrer tout les informations !");
            }

            SimpleDateFormat dcn = new SimpleDateFormat("yyyy-MM-dd");
            add.setString(1,dcn.format(datep.getDate()));
            add.setInt(2,Integer.valueOf(estim.getText()));
            add.setString(3,dcn.format(datefin.getDate()));

            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this," plan ajoutÃ© !\n ");
            selectprod();
            
            conn.close();

        }catch(SQLException e){
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1MouseClicked

